    private void setPicture(JLabel lable, String filename) {
        try {
            BufferedImage image = ImageIO.read(new File(filename));
            ImageIcon icon = new ImageIcon(image.getScaledInstance(110, 100, BufferedImage.SCALE_SMOOTH));
            Border border = BorderFactory.createLineBorder(Color.black, 1);
            lable.setIcon(icon);
            lable.setBorder(border);
        } catch (IOException ex) {
            Logger.getLogger(Picture_List.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

